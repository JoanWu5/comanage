package com.funding.crowd.pojo;

public class ProcessIntro {

	private String jointAccountId;
	private String stage;
	private int days;
	private String processImage;
	public String getJointAccountId() {
		return jointAccountId;
	}
	public void setJointAccountId(String jointAccountId) {
		this.jointAccountId = jointAccountId;
	}
	public String getStage() {
		return stage;
	}
	public void setStage(String stage) {
		this.stage = stage;
	}
	public int getDays() {
		return days;
	}
	public void setDays(int days) {
		this.days = days;
	}
	public String getProcessImage() {
		return processImage;
	}
	public void setProcessImage(String processImage) {
		this.processImage = processImage;
	}
	
	
	
}
