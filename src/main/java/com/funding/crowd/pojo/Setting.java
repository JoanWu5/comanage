package com.funding.crowd.pojo;

public class Setting {

	private String jointAccountId;
	private String upperLimitFunds;
	private String lowerLimitFunds;
	private String scaleReviewing;
	private String scaleReport;
	public String getJointAccountId() {
		return jointAccountId;
	}
	public void setJointAccountId(String jointAccountId) {
		this.jointAccountId = jointAccountId;
	}
	public String getUpperLimitFunds() {
		return upperLimitFunds;
	}
	public void setUpperLimitFunds(String upperLimitFunds) {
		this.upperLimitFunds = upperLimitFunds;
	}
	public String getLowerLimitFunds() {
		return lowerLimitFunds;
	}
	public void setLowerLimitFunds(String lowerLimitFunds) {
		this.lowerLimitFunds = lowerLimitFunds;
	}
	public String getScaleReviewing() {
		return scaleReviewing;
	}
	public void setScaleReviewing(String scaleReviewing) {
		this.scaleReviewing = scaleReviewing;
	}
	public String getScaleReport() {
		return scaleReport;
	}
	public void setScaleReport(String scaleReport) {
		this.scaleReport = scaleReport;
	}
	
	
}
