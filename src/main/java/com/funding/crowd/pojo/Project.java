package com.funding.crowd.pojo;

public class Project {

	private String jointAccountId;
	private String projectName;
	private String username;
	private String category;
	private String gruopName;
	private String breifIntro;
	private String accountId;
	private String projectImage;
	public String getJointAccountId() {
		return jointAccountId;
	}
	public void setJointAccountId(String jointAccountId) {
		this.jointAccountId = jointAccountId;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getGruopName() {
		return gruopName;
	}
	public void setGruopName(String gruopName) {
		this.gruopName = gruopName;
	}
	public String getBreifIntro() {
		return breifIntro;
	}
	public void setBreifIntro(String breifIntro) {
		this.breifIntro = breifIntro;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public String getProjectImage() {
		return projectImage;
	}
	public void setProjectImage(String projectImage) {
		this.projectImage = projectImage;
	}
	
	
}
